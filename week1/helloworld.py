# Using single quotes
print('Hello World')

# Using triple quotes (for multi-line strings)
print("""Hello World""")

# Storing in a variable first, then printing
message = "Hello World"
print(message)

# Using formatted string (f-string)
greeting = "Hello"
name = "World"
print(f"{greeting} {name}")

# Printing multiple times
print("Hello World")
print("Hello World")
print("Hello World")